<div style="padding:10px">
	<span style="font-family: 'LucidaGrande', 'Lucida Grande', 'Lucida Sans Unicode', sans-serif; font-size: 12px;">
	
<p>Bookmarks created by Stainless &quot;carry along&quot; their respective web cookies which may override cookies in other sessions.</p>
<p>For example, if you create a new Single Session tab, log into Gmail with <i>Remember me on this computer</i> checked, and then save a bookmark from your Single Session, your login credentials to Gmail will be saved along with the bookmark.</p>
<p>Session-Aware bookmarks in combination with Single Session tabs allow you to save multiple bookmarks to web sites and services where you may have more than one account.</p>
